select
    fact.packet_event_key,
    fact.packet_id,
    fact.version,
    fact.ip_header_length,
    fact.data_length,
    fact.protocol,
    fact.checksum,
    fact.src_ip,
    fact.dst_ip,
    fact.src_port,
    fact.dest_port,
    fact.control_flags,
    fact.window_size,
    fact.data,
    fact.event_epoch,
    dim.time_key,
    dim.event_date,
    dim.event_time,
    dim.calendar_year,
    dim.calendar_quarter,
    dim.calendar_month,
    dim.month_name,
    dim.day_of_month,
    dim.day_of_week,
    dim.day_name,
    dim.week_of_year,
    dim.hour_of_day,
    dim.minute_of_hour,
    dim.second_of_minute,
    dim.is_weekend
from {{ source('silver', 'FACT_PACKET_EVENTS') }} as fact
inner join {{ source('silver', 'DIM_TIME') }} as dim
    on fact.time_key = dim.time_key
